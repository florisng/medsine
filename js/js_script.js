document.querySelector("#hidden_div").style.display = "none";
function show_div() {
  document.querySelector("#hidden_div").style.display = "block";
  document.querySelector(".open_hiden_div_btn").style.display = "none";
}
function hide_div() {
  document.querySelector("#hidden_div").style.display = "none";
  document.querySelector(".open_hiden_div_btn").style.display = "block";
}